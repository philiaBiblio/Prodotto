package apli;

public class Event {
	// フィールド
	private String eventId;		// イベントID
	private String odai;		// お題
	
	// ゲッターセッター
	public String getEventId() {
		return eventId;
	}
	public void setEventId(String eventId) {
		this.eventId = eventId;
	}
	public String getOdai() {
		return odai;
	}
	public void setOdai(String odai) {
		this.odai = odai;
	}
}
